d = {"1": "!", "2":"@", "3":"#", "4":"$", "5":"%", "6":"^", "7":"&", "8":"*", "9":"(", "0":")"}
print("Подсказка: данный словарь связан с цифрами")
s = input("Введите строку: ")
user = input("Защифровать или расшифровать введенное сообщение? ")
if user == "зашифровать" or user == "Зашифровать":
    for key in d:
       s = s.replace(key, d[key])
    print("Ваше зашифрованное сообщение: ", s)
elif user == "расшифровать" or user == "Расшифровать":
    for key in d:
       s = s.replace(d[key], key)
    print("Ваше расшифрованное сообщение: ", s)
else:
    print("Есть только два варианта ответа: защифровать или расшифровать")