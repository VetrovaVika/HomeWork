"""задача
Посчитайте кол-во чисел в произвольной строке 
(число - это набор цифр, перед числом и после числа ставятся пробелы)"""
s = "aw 17 ss 18 aawe 99 awa 8 9 8 as 51 a"
s1 = s.split()
count = 0
for i, j in enumerate(s1):
    if j.isdigit():
        count += 1
if count == 0:
    print("В строке нет чисел")
else:
    print(f"В строке {count} чисел")

"""задача
Посчитайте кол-во букв в произвольной строке"""
s = "aw 17 ss 18 aawe 99 awa 8 9 8 as 51 a"
count = 0
for i, j in enumerate(s):
    if j.isalpha():
        count += 1
if count == 0:
    print("В строке нет букв")
else:
    print(f"В строке {count} букв")

